function showElementByTag(tag){
	$(tag).show();
}

function hideElementByTag(tag){
	$(tag).hide();	
}

function showElementByClass(element){
	$('.'+element).show();
}

function hideElementByClass(element){
	$('.'+element).hide();	
}

function showElementById(element){
	$('#'+element).show();
}

function hideElementById(element){
	$('#'+element).hide();	
}